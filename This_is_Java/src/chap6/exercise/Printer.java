package chap6.exercise;
/*
 * PrinterExample 클래스에서 Printer 객체를 생성하고 println() 메소드를 호출해서 매개값을 콘솔에 출력하려고 합니다.
 * println() 메소드의 매가값으로는 int, boolean, double, String 값을 줄 수 있습니다.
 * Printer클래스에서 print()메소드를 선언해보세요.
 */
public class Printer {
	
	public void println(int input) {
		System.out.println(input);
	}
	public void println(boolean input) {
		System.out.println(input);
	}
	public void println(double input) {
		System.out.println(input);
	}
	public void println(String input) {
		System.out.println(input);
	}
}
